

### Mood Checker ###
mood = input ("Hello, in which mood are you today?")

print (mood)

if mood == "happy":
    print ("It is great to see you happy!")
elif mood == "nervous":
    print ("Take a deep breath 3 times.")
elif mood == "excited":
    print ("You are excited uh?.")
elif mood == "relaxed":
    print("Nice you're relaxed.")
else:
    print("I don't recognize this mood")





